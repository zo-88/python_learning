# Band Name Generator 
# 1. ask user which city the grew up in?
city = input("What city did you grow up in?\n")

# 2. Ask user what was their name of their first pet?
pet = input("What was the name of your first pet?\n")

# 3. combine city and pet together and show them their band name
band_name = city + ' ' + pet

print(f"Your band name could be {band_name}.")